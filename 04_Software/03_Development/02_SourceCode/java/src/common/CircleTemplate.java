    /**
     * Reads the list of files from the given path and extracts the templates
     * for the circles masks.
     *
     * @param path - the path from where to read the images and transform them into circle templates
     */
    public static void readCircleTemplates(String path) {
        String comment = " /**\r\n* A mask for a filled in circle of size ";
        String template = "public static final byte[][] circleMask";

        BufferedWriter out = null;
        try {
            // open the generated circle masks file
            out = new BufferedWriter(new FileWriter(new File("generatedCircles.txt")));

            // get all the files from the given folder
            File[] listOfFiles = new File(path).listFiles();

            for (File file : listOfFiles) {
                if (file.isFile() && !file.getName().endsWith(".db")) {
                    String fileName = file.getName();

                    // extract the circle size from the 
                    String circleSize = (fileName.split("_"))[(fileName.split("_")).length - 1];
                    circleSize = circleSize.replace(".bmp", "");
                    circleSize = circleSize.replace(".png", "");

                    int diameter = Integer.parseInt(circleSize);

                    // write the comment and declaration of the mask
                    out.write(comment + diameter + "x" + diameter + ".\r\n*/");
                    out.write(template + diameter + "x" + diameter + " = {\r\n");

                    // open the image and read it by transforming it into the mask
                    BufferedImage bi = ImageIO.read(file);

                    for (int y = 0; y < bi.getHeight(); y++) {
                        out.write("{");
                        for (int x = 0; x < bi.getWidth(); x++) {
                            if (bi.getRGB(x, y) == Color.black.getRGB()) {
                                out.write("1,");
                            } else {
                                out.write("0,");
                            }
                        }
                        out.write("},\r\n");
                    }

                    out.write("};\r\n\r\n");
                }
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(CircleTemplate.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(CircleTemplate.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                if (out != null) {
                    out.close();
                }
            } catch (IOException ex) {
                Logger.getLogger(CircleTemplate.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
